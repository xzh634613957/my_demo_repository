# python数据类型

"""
Python中的数据类型可以分为两种: 数字型和非数字型
数字型:
    .整型(int)
    .浮点型(float)
    .布尔型(bool)
    .复数型(complex)
非数字型:
    .字符串(string)
    .列表
    .元组
    .字典
"""
"""
注意:
    1.type()函数可以查看变量的类型
    2.数字型的变量之间可以直接进行计算
    3.字符串之间可以通过 + 进行拼接, 通过 * 进行重复 
"""
"""
格式化字符(包含%的字符串,输出文字信息的同时输出数据):
    %s -----  字符串
    %d -----  有符号十进制整数
    %06d ---  表示输出整数的显示位数, 不足的地方用0补全
    %f -----  浮点数
    %.02f --  表示小数点后只显示两位
    %% -----  输出 %
"""