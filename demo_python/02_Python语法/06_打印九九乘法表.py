"""
    需求: 打印九九乘法表
    1. 用两个循环实现, 小循环的数字不能超过大循环
    2. 用print函数打印
"""
i = 1
while i < 10:
    j = 1
    while j <= i:
        product = j * i
        print("%d×%d=%d" % (i, j, product), end="\t")
        j += 1
    print()
    i += 1

"""
注意:
    1.在默认的情况下,print函数在输出内容之后会自动在末尾增加换行操作
    2.使用end=""可以将自动换行替换
    3. \t 为制表符, \n 为换行符, \r 为回车, \\ 为反斜杠, \' 为单引号, \" 为双引号
"""